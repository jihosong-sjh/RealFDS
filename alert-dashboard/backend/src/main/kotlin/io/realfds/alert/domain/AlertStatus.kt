package io.realfds.alert.domain

/**
 * 알림 처리 상태
 *
 * 알림의 현재 처리 상태를 나타내는 Enum입니다.
 *
 * @property UNREAD 미확인 상태 (초기 상태) - 보안 담당자가 아직 확인하지 않은 알림
 * @property IN_PROGRESS 진행 중 상태 - 보안 담당자가 알림을 확인하고 조사를 시작한 상태
 * @property COMPLETED 완료 상태 (최종 상태) - 조사가 완료되어 처리가 끝난 알림
 */
enum class AlertStatus {
    /**
     * 미확인 상태 (초기 상태)
     * 알림이 생성되었지만 아직 보안 담당자가 확인하지 않은 상태입니다.
     */
    UNREAD,

    /**
     * 진행 중 상태
     * 보안 담당자가 알림을 확인하고 조사를 시작한 상태입니다.
     * 담당자가 할당되고 조사가 진행 중임을 나타냅니다.
     */
    IN_PROGRESS,

    /**
     * 완료 상태 (최종 상태)
     * 조사가 완료되어 처리가 끝난 알림입니다.
     * 오탐으로 판단되었거나, 적절한 조치가 완료된 상태입니다.
     */
    COMPLETED
}
