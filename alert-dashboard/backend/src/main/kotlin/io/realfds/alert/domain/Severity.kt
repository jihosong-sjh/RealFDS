package io.realfds.alert.domain

/**
 * 알림 심각도
 *
 * 사기 의심 거래의 심각도를 나타내는 Enum입니다.
 * 심각도에 따라 알림의 우선순위와 대응 방식이 달라집니다.
 *
 * @property HIGH 높은 심각도 - 즉시 대응이 필요한 중요한 알림
 * @property MEDIUM 중간 심각도 - 주의가 필요한 알림
 * @property LOW 낮은 심각도 - 모니터링이 필요한 알림
 */
enum class Severity {
    /**
     * 높은 심각도
     * 즉시 대응이 필요한 중요한 알림입니다.
     * 예: 고액 거래 (1,000,000원 초과), 블랙리스트 국가 거래
     */
    HIGH,

    /**
     * 중간 심각도
     * 주의가 필요한 알림입니다.
     * 예: 해외 거래, 중간 규모 금액
     */
    MEDIUM,

    /**
     * 낮은 심각도
     * 모니터링이 필요한 알림입니다.
     * 예: 연속 거래, 패턴 이상
     */
    LOW
}
